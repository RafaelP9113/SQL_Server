select N.NATUREZA_DESCRICAO ,* 
from loja_nota_fiscal NT
JOIN dbo.LOJAS_NATUREZA_OPERACAO N
ON NT.NATUREZA_OPERACAO_CODIGO = N.NATUREZA_OPERACAO_CODIGO
where EMISSAO >= '20210301'
and NT.NATUREZA_OPERACAO_CODIGO NOT in ('6102','6152','6108','5102', '1202','2202')
ORDER BY NT.CODIGO_CLIENTE


